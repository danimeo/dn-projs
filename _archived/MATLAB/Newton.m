function N3= Newton(x,y,xp)
%此函数为牛顿二次插值函数
%   一阶差商计算
d11=(y(1)-y(2))/(x(1)-x(2))
d12=(y(2)-y(3))/(x(2)-x(3));
d13=(y(3)-y(4))/(x(3)-x(4));
%二阶差商计算
d21=(d11-d12)/(x(1)-x(3))
d22=(d12-d13)/(x(2)-x(4))
%三阶差商计算
d31=(d21-d22)/(x(1)-x(4))
N3=y(1)+d11*(xp-x(1))+d21*(xp-x(1))*(xp-x(2))+d31*(xp-x(1))*(xp-x(2))*(xp-x(3))
end
%如果xp为数组，则改为./
